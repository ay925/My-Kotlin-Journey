
//Arithmetic Operators
fun main() {
    val a=16
    val b=3
    println("Addition $a and $b is: ${a+b}")
    println("Substraction $a and $b is: ${a-b}")
    println("Multiplication $a and $b is: ${a*b}")
    println("Division of $a and $b is: ${a/b.toDouble()}")
    println("Modulo $a and $b is: ${a%b}")
}